package eu.chorevolution.transformations.generativeapproach.cdgenerator.model;

public class DataTypeWSDLInformation {
	
	private String dataTypeName;
	private String messageName;
	private String partName;
	private String elementName;
	private boolean isMainElementOfMessage;
	
	public DataTypeWSDLInformation() {
		super();
	}

	public String getDataTypeName() {
		return dataTypeName;
	}

	public void setDataTypeName(String dataTypeName) {
		this.dataTypeName = dataTypeName;
	}

	public String getMessageName() {
		return messageName;
	}

	public void setMessageName(String messageName) {
		this.messageName = messageName;
	}

	public String getPartName() {
		return partName;
	}

	public void setPartName(String partName) {
		this.partName = partName;
	}

	public String getElementName() {
		return elementName;
	}

	public void setElementName(String elementName) {
		this.elementName = elementName;
	}

	public boolean isMainElementOfMessage() {
		return isMainElementOfMessage;
	}

	public void setMainElementOfMessage(boolean isMainElementOfMessage) {
		this.isMainElementOfMessage = isMainElementOfMessage;
	}
	
}
